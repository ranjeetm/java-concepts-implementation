
public class Inheritance {

	public static void main(String[] args) {
		// inheritance = the process where one class
		// acquires, the attributes and methods of
		// another class
		
		Truck t1 = new Truck();
		t1.go();
		Bicycle b1 = new Bicycle();
		b1.stop();
		
		System.out.println(t1.doors);

	}

}
